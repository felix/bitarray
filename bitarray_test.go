package bitarray

import (
	"fmt"
	"testing"
)

func TestPack(t *testing.T) {
	tests := []struct {
		in       interface{}
		expected string
	}{
		// Types
		{uint(108), "[11011000]"},
		{uint8(108), "[11011000]"},
		{uint16(108), "[11011000]"},
		{uint32(108), "[11011000]"},
		{uint64(108), "[11011000]"},
		{int(108), "[11011000]"},
		{int8(0xF), "[11110000]"},
		{int16(108), "[11011000]"},
		{int32(108), "[11011000]"},
		{int64(108), "[11011000]"},
		{[]byte{0x6c}, "[11011000]"},
		{[]uint{108}, "[11011000]"},
		{[]int{108}, "[11011000]"},
		{[]int8{108}, "[11011000]"},
		{[]int16{108}, "[11011000]"},
		{[]int32{108}, "[11011000]"},
		{[]int64{108}, "[11011000]"},
		// Zero
		{uint(0), "[00000000]"},
		{uint8(0), "[00000000]"},
		{uint16(0), "[00000000]"},
		{uint32(0), "[00000000]"},
		{uint64(0), "[00000000]"},
		{int(0), "[00000000]"},
		{int8(0), "[00000000]"},
		{int16(0), "[00000000]"},
		{int32(0), "[00000000]"},
		{int64(0), "[00000000]"},
		{[]byte{0}, "[00000000]"},
		{[]uint{0}, "[00000000]"},
		{[]int{0}, "[00000000]"},
		{[]int8{0}, "[00000000]"},
		{[]int16{0}, "[00000000]"},
		{[]int32{0}, "[00000000]"},
		{[]int64{0}, "[00000000]"},
		// Adding a zero
		{[]int{1, 0, 23}, "[10101110]"},
		{[]uint16{1, 0, 23}, "[10101110]"},
		{[]uint32{1, 0, 23}, "[10101110]"},
		{[]uint64{1, 0, 23}, "[10101110]"},
		// Cases
		{[]uint8{0xFF, 0xFF}, "[11111111 11111111]"},
		{[]uint8{0xFF, 0xF0}, "[11111111 11110000]"},
		{[]uint8{0xF0, 0xF0}, "[11110000 11110000]"},
		{[]uint8{0xF0, 0xF0, 1}, "[11110000 11110000 10000000]"},
		{[]int{1, 128, 23}, "[11000000 01011100]"},
		{[]int{1, 129, 23}, "[11000000 11011100]"},
		{[]interface{}{uint8(0xFF), 1, 2, 1, 4, 1, 1}, "[11111111 11011001 10000000]"},
	}

	for _, tt := range tests {
		ba, err := Pack(tt.in)
		if err != nil {
			t.Fatalf("%v => failed %q", tt.in, err)
		}
		actual := fmt.Sprintf("%08b", ba.Bytes())
		if actual != tt.expected {
			t.Errorf("%v => expected %s got %s", tt.in, tt.expected, actual)
		}
		if actual != ba.String() {
			t.Errorf("%v => expected %q got %q", tt.in, actual, ba.String())
		}
	}
}
